def sigmoid(x):
    f = 10 / (1 + np.exp(-1 * x))
    return f

def softmax(array, numerator): #numeratorは分子という意味。求めたい数値のarrayでのインデックス。
    value = 0 #分母
    for i in range(len(array)):
        value = value + np.exp(array[i])
    # print("value: " + str(value))
    
    f = np.exp(array[numerator]) / value
    # print("f: " + str(f))
    return f

